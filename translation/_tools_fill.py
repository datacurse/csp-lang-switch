rows = list(csv.DictReader(open("translation/tools.csv", encoding="utf-8-sig")))
missing = sorted({r["source"] for r in rows if r["source"] not in T})
if missing:
    print("UNMAPPED:", *missing, sep="\n  ")
    sys.exit(1)

for r in rows:
    r["target"] = T[r["source"]]

with open("translation/tools.csv", "w", newline="", encoding="utf-8-sig") as f:
    w = csv.DictWriter(f, fieldnames=["key", "source", "japanese", "target"])
    w.writeheader()
    w.writerows(rows)

print(f"filled {len(rows)} rows, {len(T)} unique translations")
